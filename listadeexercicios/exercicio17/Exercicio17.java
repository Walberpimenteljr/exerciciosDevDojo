package listadeexercicios.exercicio17;

public class Exercicio17 {
    public static void main(String[] args) {


        for (int tabuada = 1; tabuada <= 10; tabuada++) {
            for (int j = 1; j <= 10; j++) {
                System.out.println(j + "X" + tabuada + "=" + j * tabuada);

            }
            System.out.println("------------");
        }
    }
}
